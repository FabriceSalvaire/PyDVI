class TextFile(object):

    ###############################################

    def parse(self, filename):

        file = open(filename, 'r')

        for line in file:
           
            comment_start_index = line.find('%')
            if comment_start_index > -1:
                line = line[:comment_start_index]

            line = line.strip()

            if len(line) > 0:
                self.parse_line(line)

        file.close()

